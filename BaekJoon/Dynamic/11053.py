# 1  2  1  1  1  1   <- 20보다 작은 수 10

# 10 20 10 30 20 50
# 1  2  1  3  1  1   <- 30보다 작은 수 10, 20  v[3] = max(v[3], v[1] + 1) 해서 3이 됨.
